#include "ft_printf.h"

int	putnbrfd(int nbr, int fd)
{
	int	i;

	i = 0;
	ft_putnbr_fd(nbr, fd);
	while (nbr != 0)
	{
		nbr /= 10;
		i++;
	}
	return (i);
}
